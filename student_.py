import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from sklearn.preprocessing import StandardScaler
import joblib

df=pd.read_csv("/content/exams.csv")
print(df.head())

print("Dataset shape:", df.shape)
print("\nColumns:", df.columns.tolist())
print("\nSummary statistics:\n", df.describe())

print("\nMissing values:\n", df.isnull().sum())

plt.figure(figsize=(6,4))
plt.scatter(df['math score'], df['reading score'])
plt.xlabel('Math Score')
plt.ylabel('Reading Score')
plt.title('Math Score vs Reading Score')
plt.grid(True)
plt.show()

X = df[['reading score', 'writing score']]
y = df['math score']

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

X_train, X_test, y_train, y_test = train_test_split(
    X_scaled, y, test_size=0.2, random_state=42
)

model = LinearRegression()
model.fit(X_train, y_train)

print("Intercept:", model.intercept_)
print("Coefficients:", model.coef_)

y_pred = model.predict(X_test)

mae = mean_absolute_error(y_test, y_pred)
mse = mean_squared_error(y_test, y_pred)
rmse = np.sqrt(mse)
r2 = r2_score(y_test, y_pred)

print(f"MAE: {mae:.2f}")
print(f"RMSE: {rmse:.2f}")
print(f"RÂ² Score: {r2:.2f}")

# Compare actual vs predicted
pd.DataFrame({"Actual": y_test.values, "Predicted": np.round(y_pred,2)}).reset_index(drop=True)

# Example: [Reading Score, Writing Score]
new_student = np.array([[75,70]])
new_student_scaled = scaler.transform(new_student)
pred_score = model.predict(new_student_scaled)
print("Predicted Final Score:", round(pred_score[0],2))

joblib.dump(model, "student_linear_model.joblib")
joblib.dump(scaler, "student_scaler.joblib")
print("Model & scaler saved.")